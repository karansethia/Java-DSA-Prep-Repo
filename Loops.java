package com.company;

public class Loops {
    public static void main(String[] args) {
        for(int i = 1;i<=5;i++){
            System.out.print(i);
        }
        int j = 1;
        while(j<=5){
            System.out.print(j);
            j++;
        }
        int n = 1;
        do{
            System.out.print(n);
            n++;
        }while(n<=5);
    }
}
